" Operators:- Arithmetic, Logical, Comparison"
 
x = 10
y = 5

" Arithmetic"
print(x+y) 
print(x-y)
print(x*y)
print(x/y)
print(x%y)
print(x**y)

" Comparison"
print(x-5 == y)
print(x != y)
print(x > y)
print(y < x)
print(x >= y)
print(y <= x)

" Logical"
print(x > y and y< x)
print(x > y or x == y)
print(not(x > y))
