" Strings:- Slicing, methods like upper(), find(), replace()"

Index = "Henry"

" Indexing"

print(Index[1])
"Note : Can access any letter or position of our value."

" Slicing"
 
print(Index[1:3])
print(Index[0:5]) or print(Index[0:5])
"Both gives same output"
print(Index[:5])

" String Important methods"

print(Index.upper())
print(Index.lower())
print(Index.capitalize())
print(Index.find("H"))
print(Index.replace("H","B"))
print(len(Index))

